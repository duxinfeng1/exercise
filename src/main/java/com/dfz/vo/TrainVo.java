package com.dfz.vo;

public class TrainVo {
    private  String mtName;
    private  Integer mtInfo;
    private String mtHard;
    private String taPart;
    private String mtTimes;
    public String getMtName() {
        return mtName;
    }

    public void setMtName(String mtName) {
        this.mtName = mtName;
    }

    public Integer getMtInfo() {
        return mtInfo;
    }

    public void setMtInfo(Integer mtInfo) {
        this.mtInfo = mtInfo;
    }

    public String getMtHard() {
        return mtHard;
    }

    public void setMtHard(String mtHard) {
        this.mtHard = mtHard;
    }

    public String getTaPart() {
        return taPart;
    }

    public void setTaPart(String taPart) {
        this.taPart = taPart;
    }

    public String getMtTimes() {
        return mtTimes;
    }

    public void setMtTimes(String mtTimes) {
        this.mtTimes = mtTimes;
    }
}
